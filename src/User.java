
public class User {

	private String username, name, image;
	
	public String getUsername (){
		return username;
	}
	public String getName (){
		return name;
	}
	public String getAvatar (){
		return image;
	}

	public void setUsername (String a){
		username = a;
	}
	public void setName (String a){
		name = a;
	}
	public void setAvatar (String a){
		image = a;
	}

}
